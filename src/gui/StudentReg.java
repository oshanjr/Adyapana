    public static String generateUniqueID() {
        Random random = new Random();
        String stid;

        do {
            int id = 100000 + random.nextInt(900000); // Generate random 6-digit number
            stid = String.valueOf(id); // Convert to String
        } while (generatedIDs.contains(stid)); // Repeat until a unique ID is found

        generatedIDs.add(stid); // Add the unique ID to the Set
        return stid; // Return the unique ID
    }

    private void loadStudent() {
        jButton5.setEnabled(false);
        jButton8.setEnabled(false);

        try {

            ResultSet resultSet = MySQL.execute("SELECT * FROM `student` "
                    + " ORDER BY `id` ASC ;");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {

                Vector<String> v = new Vector();

                v.add(resultSet.getString("st_id"));
                v.add(resultSet.getString("f_name"));
                v.add(resultSet.getString("mobile"));
                v.add(resultSet.getString("email"));
                v.add(resultSet.getString("city"));

                model.addRow(v);
                jTable1.setModel(model);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

