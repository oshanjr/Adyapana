    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        if (evt.getClickCount() == 2) {

            int selectedRow = jTable1.getSelectedRow();

            jButton8.setEnabled(true);



            String tno = String.valueOf(jTable1.getValueAt(selectedRow, 0));

            try {

                ResultSet resultSet = MySQL.execute("SELECT * FROM `teacher` WHERE `id`='" + tno + "' ;");

                while (resultSet.next()) {


                    t_fname.setText(resultSet.getString("f_name"));
                    t_lname.setText(resultSet.getString("l_name"));
                    t_mobile.setText(resultSet.getString("mobile"));
                    t_email.setText(resultSet.getString("email"));
                    t_addr.setText(resultSet.getString("address"));
                    t_nic.setText(resultSet.getString("nic"));
                    t_city.setText(resultSet.getString("city"));

                }

                jButton5.setEnabled(true);

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jTable1MouseClicked

