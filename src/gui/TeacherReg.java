    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed

        int selectedRow = jTable1.getSelectedRow();
        String id = String.valueOf(jTable1.getValueAt(selectedRow, 0));

        String fname = t_fname.getText();
        String lname = t_lname.getText();
        String email = t_email.getText();
        String addr = t_addr.getText();
        String city = t_city.getText();
        String mobile = t_mobile.getText();
        String nic = t_nic.getText();

        try {
            MySQL.execute("UPDATE `teacher` SET `f_name`='" + fname + "', "
                    + "`l_name`='" + lname + "', "
                    + "`mobile`='" + mobile + "', "
                    + "`email`='" + email + "', "
                    + "`nic`='" + nic + "', "
                    + "`address`='" + addr + "', "
                    + "`city`='" + city + "' "
                    + "WHERE `id`='" + id + "'");

            jTable1.setEnabled(true);
            loadTeachers();
            reset();
        } catch (Exception e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_jButton8ActionPerformed

    
